#! /bin/sh

# To set up the ZAIN VPN:
# 0. Download F5
# 1. Rename Makefile.txt to Makefile
# 2. Run 'make install'
# 3. As root, run f5vpn-login user@machine
# 4. Link this script into /etc/init.d e.g. > ln -s /usr/local/my_project/deploy/vpn/zain-vpn.sh /etc/init.d/zain-vpn
# 5. Add it to the runlevels, on Ubuntu/Debian there is a nice tool to do this for you:
#    > sudo update-rc.d zain-vpn defaults
#
 
